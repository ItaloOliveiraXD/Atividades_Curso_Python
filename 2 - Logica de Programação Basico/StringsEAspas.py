"""
str - String.
String é um texto.
Números entre aspas('',"") é considerado string(texto) no Python.
"""

print('Hello World!')
print("Olá mundo!")
print('123456')
print(123456)  # Isso é números inteiros.
print('Essa é uma "String" (str).')
print("Essa é uma 'String' (str).")
print('Esse é um \'texto\' (str).')
print("Esse é um \"texto\" (str) ")
