#positivo   [0123456789]
string =    'Python_bom'
#negativo -[10987654321]

#Forma de fatiar a string.
print(string[0:6])  # índice 0 ao índice 5

#Outra forma de fatiar a string.
print(string[0:-4])  #índice 0 ao índice negativo -4

#Fatiar string pulando índices.
#            partida:fim:passo
print(string[0      :9  :2])

