"""
Operadores Lógicos
--> and, or, not
--> in e not in
"""
# Operador lógico  AND.
print('Operador lógico E (and).')
print('True and True =', True and True)
print('True and False =', True and False)
print('---------------------------')

# Operador lógico OU.
print('Operador lógico OU (or).')
print('True or True =', True or True)
print('True or False =', True or False)
print('False or False =', False or False)
print('---------------------------')

# Operador lógico NOT.
print('Operador lógico NOT (not), inverte o valor original.')
print('not True =', not True)
print('not False =', not False)
print('---------------------------')

# Operador lógico Contém (in).
print('Operador lógico Contém (in)')
print("'I' in 'Italo'? ", 'I' in 'Italo')
print("'U' in 'Italo'? ", 'U' in 'Italo')
print('---------------------------')

# Operador lógico que inverte Contém (not in).
print('Operador lógico que inverte Contém (not in).')
print("'I'  not in 'Italo'? ", 'I' not in 'Italo')
print("'U'  not in 'Italo'? ", 'U' not in 'Italo')
